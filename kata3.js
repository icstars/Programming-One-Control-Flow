// Write a multgiplication table from 1 to 5.

let number = 1;
console.log(`Multiplication Table for ${number}`);
for (let i = 1; i <= 5; i++) {
    console.log(`${number} * ${i} = ${number * i}`);
}
for (let DC = 5; DC >=1; DC--) {
    for (let C = 1; C <= 5; C++){
console.log(`${DC} x ${C}= ${DC* C}`);
    }
       
console.log('-----');
}
